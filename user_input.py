def get_time():
    time = input("Enter a number from 1 to 12: ")
    return time

def get_noun():
    noun = input("Enter a noun (plural): ")
    return noun

def get_name():
    name = input("Enter a name: ")
    return name.title()

def get_sentence():
    sentence = input("Enter any sentence: ")
    return sentence.upper()

def get_verb():
    verb = input("Enter a verb: ")
    return verb

